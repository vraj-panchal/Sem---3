#include <iostream>

using namespace std;

int main()
{
    // datatype
    int x, y;

    // input

    cout << "Enter First Number : " << endl;
    cin >> x;

    cout << "Enter Secound Number : " << endl;
    cin >> y;

    // output & print

    cout << "x + Y =" << x + y << endl;
    cout << "x - Y =" << x - y << endl;
    cout << "x * Y =" << x * y << endl;
    cout << "x / Y =" << (float)x / y << endl;

    return 0;
}




